/**
* Default score state of a user
*/
function defaultScoreState() {
    return {
        calories: 400,      // 400 calories
        fat: 13,            // 13g
        cholesterol: 60,    // 60mg
        sodium: 480,        // 480mg
        carbohydrate: 60,   // 60g
        protein: 10,        // 10g
        sugar: 18           // 18g
    };
}

